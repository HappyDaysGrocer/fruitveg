/* =========================================================================
   CATALOGUE — loaded from ./catalog.js (non-module global HD_CATALOG),
   a PUBLIC, COST-FREE product list generated by app2/build-catalog.cjs.
   Row shape: { c: category, n: name, s: sellPrice, b: barcode }.
   There is deliberately NO cost field anywhere in the customer app —
   purchase costs / wholesale costs / margins never reach this bundle.
   ========================================================================= */

let CATALOG = [];
const CATALOG_BY_KEY = new Map();
let _catalogPromise = null;

function buildCatalog(rows) {
  CATALOG = [];
  CATALOG_BY_KEY.clear();
  (Array.isArray(rows) ? rows : []).forEach((r) => {
    if (!r || typeof r !== 'object') return;
    const cat = String(r.c == null ? '' : r.c).trim();
    const name = String(r.n == null ? '' : r.n).trim();
    if (!name) return;
    const item = {
      key: cat + '||' + name,         // this exact key joins all stores
      cat,
      group: String(r.g || cat).trim(), // Woolies-style aisle (display only)
      name,
      sell: num(r.s),                 // retail shelf price only — no cost
      barcode: r.b == null ? '' : String(r.b).trim()
    };
    CATALOG.push(item);
    CATALOG_BY_KEY.set(item.key, item);
  });
}

/* Inject <script src="catalog.js"> (it is NOT a module) and wait for
   window.HD_CATALOG. Caches the rows in localStorage so the catalogue
   still works on an offline cold start. */
function loadCatalog() {
  return new Promise((resolve) => {
    if (window.HD_CATALOG) return resolve(window.HD_CATALOG);
    const s = document.createElement('script');
    s.src = 'catalog.js';
    let settled = false;
    const finish = (rows) => { if (!settled) { settled = true; resolve(rows); } };
    s.onload = () => {
      // The global is set synchronously by the script, but poll briefly
      // as cheap insurance against any deferred assignment.
      let tries = 0;
      const tick = () => {
        if (window.HD_CATALOG) return finish(window.HD_CATALOG);
        if (++tries > 60) return finish(null);   // ~3s then give up
        setTimeout(tick, 50);
      };
      tick();
    };
    s.onerror = () => finish(null);              // offline / 404
    document.head.appendChild(s);
  });
}

export async function initCatalog() {
  if (CATALOG.length) return CATALOG;            // idempotent
  if (_catalogPromise) return _catalogPromise;
  _catalogPromise = (async () => {
    const rows = await loadCatalog();
    if (Array.isArray(rows) && rows.length) {
      LS.set('hd2.catalogCache2', rows);         // cost-free cache (v2 key)
      buildCatalog(rows);
    } else {
      buildCatalog(LS.get('hd2.catalogCache2', []));
    }
    LS.del('hd2.catalogCache');                  // purge old cost-bearing cache
    _applySecureCache();                          // v3: overlay any cached costs (offline)
    const _ch = LS.get('hd3.hist', null); if (_ch) _hist = _ch;   // offline history
    BUS.emit('change');
    return CATALOG;
  })();
  return _catalogPromise;
}

