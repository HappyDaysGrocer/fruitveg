export function marginInfo(key) {
  const it = CATALOG_BY_KEY.get(key);
  if (!it) return null;
  const cost = (typeof it.cost === 'number') ? it.cost : null;
  const sell = (typeof it.sell === 'number') ? it.sell : null;
  let profit = null, marginPct = null;
  if (cost != null && sell != null && sell > 0) {
    profit = round2(sell - cost);
    marginPct = Math.round((profit / sell) * 100);
  }
  return { cost, sell, profit, marginPct };
}

