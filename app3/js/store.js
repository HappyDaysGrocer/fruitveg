/**
 * Auto-place standing orders. For each active standing order, find the next
 * delivery date (run cut-off still open, weekday in the standing weekdays);
 * if not yet generated for that date, place a real order priced at TODAY'S
 * tier prices. Deterministic order id (standingId + date) makes generation
 * idempotent across devices — concurrent runs rewrite the same record.
 * Called on boot and after every pull(); cheap no-op when nothing is due.
 */
export function generateStandingOrders() {
  if (!CATALOG.length) return 0;                  // prices not loaded yet
  let made = 0;
  for (const s of Object.values(mirror.standing)) {
    if (!s || s.active === false) continue;
    if (!Array.isArray(s.lines) || !s.lines.length) continue;
    if (!Array.isArray(s.weekdays) || !s.weekdays.length) continue;
    const cust = mirror.customers[s.custId] ||
      Object.values(mirror.customers).find((c) => c && c.id === s.custId);
    if (!cust) continue;
    const run = (cust.runId && mirror.runs[cust.runId]) ||
      Object.values(mirror.runs).find((r) => r && r.active !== false);
    const di = deliveryInfo(run, null, s.weekdays);
    if (!di) continue;
    if (s.lastGenerated && s.lastGenerated >= di.date) continue;   // already done
    const oid = 'so' + s.id + '_' + di.date.replace(/-/g, '');
    if (!mirror.orders[oid]) {
      const lines = s.lines.map((l) => {
        const item = CATALOG_BY_KEY.get(l.key);
        const tp = tierPrice(s.custId, l.key);
        return {
          key: l.key,
          name: (item && item.name) || l.name || l.key,
          sup: (item && item.cat) || l.sup || '',
          unit: l.unit || '',
          qty: Number(l.qty) || 0,
          price: typeof tp === 'number' ? tp : '',
          src: 'tier'
        };
      }).filter((l) => l.qty > 0);
      if (!lines.length) continue;
      patch('custorders', oid, {
        id: oid,
        custId: s.custId,
        runId: cust.runId || (run && run.id) || '',
        date: todayStr(),
        status: 'completed',
        completed: todayStr(),
        placedAt: Date.now(),
        deliveryDate: di.date,
        orderNo: di.date.replace(/-/g, '') + '-R' + String(s.id).slice(-4),
        channel: 'standing',
        lines
      });
      fbPatch('ordersByCustomer/' + s.custId, { [oid]: true });
      made++;
    }
    s.lastGenerated = di.date;
    patch('standing', s.id, s);
  }
  if (made) BUS.emit('change');
  return made;
}

/** Active promo (flat price for all customers) on a product, or null. */
export function specialFor(key) {
  const today = todayStr();
  for (const s of Object.values(mirror.specials)) {
    if (!s || s.key !== key) continue;
    if (s.price == null || s.price === '') continue;   // cleared = inactive
    if (s.until && s.until < today) continue;           // expired
    return s;
  }
  return null;
}

export function saveOrder(o) {
  if (!o.id) o.id = genId('o');
  if (!Array.isArray(o.lines)) o.lines = [];
  patch('custorders', o.id, o);
  // Fan-out index so a customer login can list ONLY its own orders
  // (RTDB rules can't filter a whole-node read).
  if (o.custId) fbPatch('ordersByCustomer/' + o.custId, { [o.id]: true });
  BUS.emit('change');
  return o;
}

/** Find this customer's open order, or create one dated today. */
export function ensureOpenOrder(custId) {
  const existing = Object.values(mirror.orders).find(
    (o) => o && o.custId === custId && o.status === 'open'
  );
  if (existing) return existing;
  return saveOrder({
    id: genId('o'),
    custId,
    date: todayStr(),
    status: 'open',
    lines: []
  });
}

/* ---------- tier pricing ---------- */

/**
 * Price for one catalogue item for one customer.
 *   shop     -> sell
 *   shopAdj  -> round2(sell * (1 + pct/100))
 *   costPlus -> sell  (the customer app carries NO cost, so cost-plus tiers
 *               fall back to the shelf price; staff set per-customer prices
 *               via customer.prices[key], which are PRICES, never costs)
 *   manual   -> ''
 * Resolution order: per-customer price -> active special (flat promo) ->
 * the customer's price-level/group rule -> shelf.
 * Returns number | ''.
 */
export function tierPrice(custId, key) {
  const cust = mirror.customers[custId] ||
    Object.values(mirror.customers).find((c) => c && c.id === custId) || null;

  if (cust && cust.prices && cust.prices[key] != null && cust.prices[key] !== '') {
    const p = num(cust.prices[key]);
    if (p != null) return round2(p);
  }

  // An active special is a flat promo price for everyone (below per-customer).
  const sp = specialFor(key);
  if (sp) { const v = num(sp.price); if (v != null) return round2(v); }

  const tierId = (cust && cust.tierId) || 'retail';
  const tier = mirror.tiers[tierId] || DEFAULT_TIERS[tierId] ||
    mirror.tiers.retail || DEFAULT_TIERS.retail;
  const rule = (tier && tier.rule) || { type: 'shop' };

  const item = CATALOG_BY_KEY.get(key);
  if (!item) return '';

  const pct = Number(rule.pct) || 0;
  switch (rule.type) {
    case 'shop':
      return item.sell == null ? '' : item.sell;
    case 'shopAdj':
      return item.sell == null ? '' : round2(item.sell * (1 + pct / 100));
    case 'costPlus':
      // No cost in the customer app — fall back to the shelf price.
      return item.sell == null ? '' : item.sell;
    case 'manual':
    default:
      return '';
  }
}

/* ---------- till queue ---------- */

export function queueForTill(order, custName, eposCustId) {
  const qid = genId('tq');
  const lines = (order.lines || []).map((l) => ({
    key: l.key, name: l.name, qty: l.qty, price: l.price, unit: l.unit || ''
  }));
  const total = Math.round(
    lines.reduce((s, l) => s + (Number(l.qty) || 0) * (Number(l.price) || 0), 0) * 100
  ) / 100;
  const rec = {
    id: qid,
    orderId: order.id,
    custId: order.custId,
    custName: custName || '',
    eposCustId: eposCustId || null,
    status: 'queued',
    lines,
    total,
    ref: order.orderNo || order.id,
    queuedAt: new Date().toISOString(),
    tillTxnId: null,
    error: null,
    by: (_auth && _auth.email) ? String(_auth.email).split('@')[0] : ''
  };
  patch('tillqueue', qid, rec);
  BUS.emit('change');
  return rec;
}

export function tillQueueStatus(orderId) {
  for (const r of Object.values(mirror.tillqueue)) {
    if (r && r.orderId === orderId) return r;
  }
  return null;
}

export function tillqueue() {
  return Object.values(mirror.tillqueue).filter(Boolean)
    .sort((a, b) => String(b.queuedAt || '').localeCompare(String(a.queuedAt || '')));
}
