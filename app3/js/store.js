function friendlyAuthError(code) {
  code = String(code || '');
  if (code.indexOf('EMAIL_NOT_FOUND') === 0 ||
      code.indexOf('INVALID_PASSWORD') === 0 ||
      code.indexOf('INVALID_LOGIN_CREDENTIALS') === 0) return 'Wrong username or password.';
  if (code.indexOf('TOO_MANY_ATTEMPTS') === 0) return 'Too many attempts — wait a minute and try again.';
  if (code.indexOf('USER_DISABLED') === 0) return 'This account has been disabled.';
  return 'Could not sign in. Check your connection and try again.';
}

export const auth = {
  async login(username, password) {
    const u = String(username || '').trim();
    const email = u.indexOf('@') >= 0 ? u : u + '@happydaysgrocer.app';
    const res = await fetch(
      'https://identitytoolkit.googleapis.com/v1/accounts:signInWithPassword?key=' + FB.apiKey,
      {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ email, password, returnSecureToken: true })
      }
    );
    let data = {};
    try { data = await res.json(); } catch (e) { /* fall through */ }
    if (!res.ok) {
      throw new Error(friendlyAuthError(data && data.error && data.error.message));
    }
    const blob = {
      idToken: data.idToken,
      refreshToken: data.refreshToken,
      // refresh 60s early so an in-flight request never carries a dead token
      expiresAt: Date.now() + (Number(data.expiresIn) || 3600) * 1000 - 60000,
      uid: data.localId,
      email
    };
    // Customer login? /custUsers/<uid> maps a Firebase uid to its customer
    // record (written by staff when they create the login). Best effort —
    // until the security rules are published this just comes back denied.
    try {
      const r = await fetch(FB.databaseURL + '/custUsers/' +
        encodeURIComponent(data.localId) + '.json?auth=' + encodeURIComponent(data.idToken));
      if (r.ok) {
        const cid = await r.json();
        if (typeof cid === 'string' && cid) blob.custId = cid;
      }
    } catch (e) { /* offline / rules not live yet */ }
    setAuth(blob);
    return auth.user();
  },

  logout() { setAuth(null); },

  /** Set a new password for the signed-in account (the phone's live session
      authorises it — no old password needed). Returns the fresh tokens into
      the auth blob so the session continues uninterrupted. */
  async changePassword(newPassword) {
    const t = await auth.token();
    if (!t) throw new Error('Sign in first.');
    const res = await fetch(
      'https://identitytoolkit.googleapis.com/v1/accounts:update?key=' + FB.apiKey,
      {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ idToken: t, password: newPassword, returnSecureToken: true })
      }
    );
    let data = {};
    try { data = await res.json(); } catch (e) { /* fall through */ }
    if (!res.ok) {
      const code = String((data && data.error && data.error.message) || '');
      if (code.indexOf('CREDENTIAL_TOO_OLD') === 0 || code.indexOf('TOKEN_EXPIRED') === 0) {
        throw new Error('For safety, sign out and sign back in, then change the password.');
      }
      if (code.indexOf('WEAK_PASSWORD') === 0) throw new Error('Password too short — use 6+ characters.');
      throw new Error('Could not change the password. Check your connection and try again.');
    }
    setAuth({
      idToken: data.idToken || t,
      refreshToken: data.refreshToken || _auth.refreshToken,
      expiresAt: Date.now() + (Number(data.expiresIn) || 3600) * 1000 - 60000,
      uid: data.localId || _auth.uid,
      email: _auth.email,
      custId: _auth.custId || undefined
    });
    return true;
  },

  user() {
    if (!_auth) return null;
    return {
      uid: _auth.uid, email: _auth.email,
      name: String(_auth.email || '').split('@')[0],
      custId: _auth.custId || null
    };
  },

  /** Resolves a valid idToken (auto-refresh) or null when signed out. */
  async token() {
    if (!_auth) return null;
    if (Date.now() < _auth.expiresAt) return _auth.idToken;
    try {
      const res = await fetch('https://securetoken.googleapis.com/v1/token?key=' + FB.apiKey, {
        method: 'POST',
        headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
        body: 'grant_type=refresh_token&refresh_token=' + encodeURIComponent(_auth.refreshToken)
      });
      if (!res.ok) {
        // 400/401/403 => refresh token revoked/invalid: sign out cleanly.
        if (res.status === 400 || res.status === 401 || res.status === 403) {
          setAuth(null);
          return null;
        }
        return _auth.idToken; // transient server error: try the stale token
      }
      const d = await res.json();
      setAuth({
        idToken: d.id_token,
        refreshToken: d.refresh_token || _auth.refreshToken,
        expiresAt: Date.now() + (Number(d.expires_in) || 3600) * 1000 - 60000,
        uid: d.user_id || _auth.uid,
        email: _auth.email,
        custId: _auth.custId || undefined   // keep the customer binding
      });
      return _auth.idToken;
    } catch (e) {
      return _auth.idToken; // offline: stale token; callers tolerate failure
    }
  }
};

